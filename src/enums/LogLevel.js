import Enum from './Enum';

/**
 * Enum containing the possible different values for {@link Logger#logLevel}
 * @enum {string}
 * @property {number} DEBUG - Show message that have a priority equal or greater then debug
 * @property {number} INFO - Show message that have a priority equal or greater then info
 * @property {number} WARN - Show message that have a priority equal or greater then a warning
 * @property {number} NONE - Show no messages
 * @readonly
 */
export const LogLevel = new Enum(['debug', 'info', 'warn', 'none'].map(x => x.toUpperCase()));
